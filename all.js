function addHere(elem){

    let image=elem.image;
    let name=elem.name;
    let info=elem.info;
    let price=elem.price

    let addObj={

        image:image,
        name:name,
        info:info,
        price:price,
    }

    addArr.push(addObj);
    localStorage.setItem("cartData",JSON.stringify(addArr));
    
}
    


